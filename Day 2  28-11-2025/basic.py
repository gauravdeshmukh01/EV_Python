# python identity operator 

# is - return True if both variables point to the same object, False otherwise.  x is y
# is not - return True if both variables do not point to the same object, False otherwise. x is not y

# examples:
# a = ["apple", "banana"]
# b = a
# print(a is b)      # returns True
# print(a is not b)  # returns False

# *******************************************************

# python membership operator

# in - return True if a sequence with the specified value is present in the object.  x in y
# not in - return True if a sequence with the specified value is not present in the object.  x not in y
# examples:
# a = ["apple", "banana"]
# print("banana" in a)   # returns True
# print("pineapple" not in a)  # returns True

